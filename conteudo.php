        <h1 class="titulos">Maconha da boa no mercado</h1>
        <img src="imagens/postagem1.jpg" class="imagem">
        <p class="paragrafo">Cannabis é um gênero de angiospermas que inclui três variedades diferentes: Cannabis sativa, Cannabis indica e Cannabis ruderalis. Estes três táxons são nativos do Centro e do Sul da Ásia. A Cannabis tem sido muito utilizada para a fabricação de fibras (cânhamo), para sementes e óleos de sementes, para fins medicinais e como droga psicoativa. Os produtos industriais de cânhamo são feitos a partir de plantas de Cannabis selecionadas para produzir uma grande quantidade de fibras. Para cumprir a Convenção de Narcóticos da ONU, algumas cepas de cannabis foram criadas para produzir níveis mínimos de THC, o principal constituinte psicoativo responsável pelo "barato" associado com a maconha, que consiste em flores secas de plantas de cannabis criadas de forma selecionada para produzir altos níveis de THC e de outros canabinoides psicoativos. Diversos produto derivados, incluindo extratos de haxixe, são também produzidos a partir da planta.</p>
